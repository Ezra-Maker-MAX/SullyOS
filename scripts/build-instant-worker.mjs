#!/usr/bin/env node
// node >= 20
import { build } from 'esbuild';
import { existsSync, statSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

const entryPoint = resolve(root, 'worker/instant-push/src/index.ts');
if (!existsSync(entryPoint)) {
  console.error(`ERROR: entry point not found: ${entryPoint}`);
  process.exit(1);
}

const outWorker = resolve(root, 'worker/instant-push/worker.bundle.js');
const outPublic = resolve(root, 'public/instant-worker.bundle.js');

console.log('Building instant-push worker...');

const sharedOpts = {
  format: 'esm',
  target: 'es2022',
  platform: 'neutral',
  bundle: true,
  minify: false,
  // node:crypto / crypto are provided by CF Workers runtime (nodejs_compat flag)
  external: ['node:crypto', 'crypto'],
  banner: { js: '/* generated by scripts/build-instant-worker.mjs — do not edit by hand */' },
  conditions: ['worker', 'browser', 'import', 'default'],
};

await build({ ...sharedOpts, entryPoints: [entryPoint], outfile: outWorker });
// Copy to public/ for the frontend "Copy Worker code" button
await build({ ...sharedOpts, entryPoints: [entryPoint], outfile: outPublic });

const workerSize = statSync(outWorker).size;
const publicSize = statSync(outPublic).size;
console.log(`✓ worker/instant-push/worker.bundle.js  ${(workerSize / 1024).toFixed(1)} KB`);
console.log(`✓ public/instant-worker.bundle.js       ${(publicSize / 1024).toFixed(1)} KB`);
